
//1: Write a java program to check if a number is even or odd.
class EvenOdd{
	public static void main(String[] pp){
		int a=5;
		if(a%2==0){
			System.out.println(a+" is an even number.");
		}
		else if(a%2==1){
			System.out.println(a+" is an odd number.");
		}
		else{
			System.out.println(a+" is an even number.");
		}
	}
}
