class ArrayDemo{
	public static void main(String [] args){
		int arr[]={10,20,30,40};
		System.out.println(arr[0]);
		System.out.println(arr[1]);
		System.out.println(arr[2]);
		System.out.println(arr[3]);
		System.out.println("For looop");
		for(int i=0;i<4;i++){
			System.out.println(arr[i]);
		}
	}
}

